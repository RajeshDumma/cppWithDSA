#include<iostream>
using namespace std;

// Logical operator , OR(||), AND(&&), NOT(!)
int main(){

     bool a, b;
     a = true;
     b = false;

     cout<<" a || b? "<< (a || b) << endl;
     cout<<" a && b? "<< (a && b) << endl;
     cout<<" !a ? "<< (!a) << endl;
     cout<<" !b ? "<< (!b) << endl;
    return 0;
}