#include<iostream>
using namespace std;

// Relational operators
int main(){

     int a , b;
     cout<<"Enter the a value: "<<endl;
     cin>> a;
     cout<<"Enter the b value: "<<endl;
     cin>> b;
     cout<<(a == b)<<endl; // 0
     cout<<(a != b)<<endl; // 1
     cout<<(a > b)<<endl; // 0
     cout<<(a < b)<<endl; // 1
     cout<<(a >= b)<<endl; // 0
     cout<<(a <= b)<<endl; // 1
   return 0;
}